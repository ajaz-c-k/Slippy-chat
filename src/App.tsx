import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";

import Index from "./pages/Index";
import NotFound from "./pages/NotFound";
import { ChatPage } from "@/components/ChatPage";

const queryClient = new QueryClient();

// Wrapper for ChatPage to pull data from sessionStorage
const ChatPageWrapper = () => {
  const username = sessionStorage.getItem("slippy:name");
  const avatar = sessionStorage.getItem("slippy:avatar");

  if (!username || !avatar) {
    // No login data â†’ go back to Index
    return <Index />;
  }

  const currentUser = {
    id: Date.now().toString(),
    username,
    avatar,
    online: true,
  };

  return (
    <ChatPage
      currentUser={currentUser}
      onLogout={() => {
        sessionStorage.clear();
        window.location.href = "/";
      }}
    />
  );
};

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<Index />} />
          <Route path="/chat" element={<ChatPageWrapper />} />
          {/* Catch-all route */}
          <Route path="*" element={<NotFound />} />
        </Routes>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
